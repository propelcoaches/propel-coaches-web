-- PROGRESSION TREE SEEDS — Linking System (using exercise names)
-- These establish the linked exercise system referenced in progressions/regressions arrays
-- ══════════════════════════════════════════════════════════════════════════════

-- After all exercises are inserted, create a view that makes progression trees queryable
create or replace view exercise_progression_tree as
select
  e.id,
  e.name,
  e.category,
  e.difficulty_level,
  e.progressions,
  e.regressions,
  e.tags,
  e.primary_muscle_group,
  e.is_compound,
  e.force_type,
  e.plane_of_motion,
  e.is_unilateral,
  e.coaching_cues,
  e.contraindications
from exercises e
where e.is_system = true;

-- Smart search view: find alternatives by equipment, muscle, or difficulty
create or replace view exercise_alternatives as
select
  a.id,
  a.name as exercise_name,
  a.category,
  a.primary_muscle_group,
  a.difficulty_level,
  a.equipment,
  b.id as alternative_id,
  b.name as alternative_name,
  b.equipment as alternative_equipment,
  b.difficulty_level as alternative_difficulty
from exercises a
join exercises b on (
  a.primary_muscle_group = b.primary_muscle_group
  and a.movement_type = b.movement_type
  and a.id != b.id
  and a.is_system = true
  and b.is_system = true
);

